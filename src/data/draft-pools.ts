export function draftPoolKey(pool: DraftPool): string {
  return `${pool.game}:${pool.teamId}`
}

export function parseDraftPoolKey(key: string): DraftPool {
  const i = key.indexOf(':')
  const game = key.slice(0, i) as GameId
  const teamId = key.slice(i + 1)
  const found = DRAFT_POOLS.find(p => p.game === game && p.teamId === teamId)
  return found ?? { game, teamId, label: `${teamId} (${game})` }
}

export function displayPoolLabel(pool: DraftPool | string): string {
  if (typeof pool === 'string') return parseDraftPoolKey(pool).label
  return pool.label
}

const DRAFT_POOL_BLOCKLIST = new Set(['Unaffiliated', 'Sub Character'])

/** Pools jouables au draft (sans fourre-tout zukan) */
export function getPlayableDraftPools(): DraftPool[] {
  return DRAFT_POOLS.filter(p => !DRAFT_POOL_BLOCKLIST.has(p.teamId))
}
